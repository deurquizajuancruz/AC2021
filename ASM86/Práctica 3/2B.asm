PA EQU 30H
PB EQU 31H
CA EQU 32H
CB EQU 33H

ORG 1000H
MENSAJE DB "ORGANIZACION Y "
        DB "ARQUITECTURA DE "
        DB "COMPUTADORAS"
FIN DB ?

ORG 2000H
MOV AL,1
OUT CA,AL
DEC AL
OUT CB,AL
IN AL,PA
AND AL,0FDH
OUT PA,AL
MOV BX,OFFSET MENSAJE
MOV CL,OFFSET FIN - OFFSET MENSAJE
POLL: IN AL,PA
AND AL,1
JNZ POLL
MOV AL,[BX]
OUT PB,AL
IN AL,PA
OR AL,2
OUT PA,AL
IN AL,PA
AND AL,0FDH
OUT PA,AL
INC BX
DEC CL
JNZ POLL
INT 0
END
