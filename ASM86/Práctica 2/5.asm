ORG 1000H
MSJ DB "INGRESE UN NUMERO: "
FIN DB ?
MS2 DB " CARACTER NO VALIDO"
IMP DB ?
ORG 1500H
NUM DB ?
ORG 3000H
ES_NUM:CMP BYTE PTR [BX],30H
JS NONUM
JZ NUMERO
CMP BYTE PTR [BX],39H
JNS NONUM
JS NUMERO
JZ NUMERO
NUMERO: MOV BX,0FFH
JMP TER
NONUM:MOV BX,OFFSET MS2
MOV AL,OFFSET IMP - OFFSET MS2
INT 7
MOV BX,0
TER:RET
ORG 2000H
MOV BX,OFFSET MSJ
MOV AL,OFFSET FIN - OFFSET MSJ
INT 7
MOV BX,OFFSET NUM
INT 6
MOV AL,1
INT 7
MOV CL,NUM
MOV BX,OFFSET NUM
CALL ES_NUM
INT 0
END
