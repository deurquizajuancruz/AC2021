ORG 1000H
NUMS DB "CERO  "
     DB "UNO   "
     DB "DOS   "
     DB "TRES  "
     DB "CUATRO"
     DB "CINCO "
     DB "SEIS  "
     DB "SIETE "
     DB "OCHO  "
     DB "NUEVE "
MSJ DB "INGRESE UN NUMERO: "
FIN DB ?
ORG 1500H
NUM DB ?
ORG 2000H
MOV CX,0
LAZO: MOV BX,OFFSET MSJ
MOV AL,OFFSET FIN - OFFSET MSJ
INT 7
MOV BX,OFFSET NUM
INT 6
CMP NUM,30H
JNZ NO_CERO
INC CX
JMP SEGUIR
NO_CERO: MOV CX,0
SEGUIR: MOV BX,OFFSET NUMS
MOV AL,6
LOP: CMP NUM,30H
JZ IMPRIME
ADD BX,6
DEC NUM
JMP LOP
IMPRIME: INT 7
CMP CX,2
JNZ LAZO
INT 0
END
